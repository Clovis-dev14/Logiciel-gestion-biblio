            private void loadBooks() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Vide le tableau avant de le charger

        try (Connection conn = DBConnexion.getConnection(); // Connexion via DBConnexion
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery("SELECT * FROM livres")) {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_livre"),
                    rs.getString("titre"),
                    rs.getString("auteur"),
                    rs.getString("genre"),
                    rs.getInt("AnneePublication"),
                    rs.getBoolean("disponible") ? "Oui" : "Non"
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erreur lors du chargement des livres !");
            ex.printStackTrace();
        }
    }
       
     

