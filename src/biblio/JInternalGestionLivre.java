    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        // TODO add your handling code here:
        btnAjouter.addActionListener(e -> {
    int id = Integer.parseInt(txtID.getText()); // Récupère l'ID en tant qu'entier
    String titre = txtTitre.getText(); // Récupère le texte de la zone "Titre"
    String auteur = txtAuteur.getText(); // Récupère le texte de la zone "Auteur"
    String genre = txtGenre.getText(); // Récupère le texte de la zone "Auteur"
    String annee = txtAnnee.getText(); // Récupère l'année entrée
    boolean disponible = chkDisponible.isSelected(); // Vérifie l'état de la case à cocher

    if (titre.isEmpty() || auteur.isEmpty() || annee.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs !");
        return; // Quitte la méthode si des champs sont vides
    }

    try (Connection conn = DBConnexion.getConnection();
         PreparedStatement stmt = conn.prepareStatement(
             "INSERT INTO livres (id_livre, titre, auteur, genre, AnneePublication, disponible) VALUES (?, ?, ?, ?, ?, ?)"
         )) {
         stmt.setInt(1, id);
            stmt.setString(2, titre);
            stmt.setString(3, auteur);
            stmt.setString(4, genre);
            stmt.setInt(5, Integer.parseInt(annee));
            stmt.setBoolean(6, disponible);

            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Livre ajouté avec succès !");
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout du livre !");
    }
});


    }//GEN-LAST:event_btnAjouterActionPerformed

    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        // TODO add your handling code here:
    int selectedRow = jTable1.getSelectedRow(); // Vérifie la sélection dans le tableau
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un livre à modifier !");
        return;
    }

    // Récupère les données actuelles et modifiées
    int idLivre = (int) jTable1.getValueAt(selectedRow, 0); // ID du livre
    String nouveauTitre = txtTitre.getText();
    String nouvelAuteur = txtAuteur.getText();
    String nouvelleAnnee = txtAnnee.getText();
    String nouveauGenre = txtGenre.getText();
    boolean disponible = chkDisponible.isSelected();

    // Vérification des champs
    if (nouveauTitre.isEmpty() || nouvelAuteur.isEmpty() || nouvelleAnnee.isEmpty() || nouveauGenre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs !");
        return;
    }

    try (Connection conn = DBConnexion.getConnection();
         PreparedStatement stmt = conn.prepareStatement(
             "UPDATE livres SET titre = ?, auteur = ?, AnneePublication = ?, disponible = ?, genre = ? WHERE id_livre = ?"
         )) {
        stmt.setString(1, nouveauTitre);
        stmt.setString(2, nouvelAuteur);
        stmt.setInt(3, Integer.parseInt(nouvelleAnnee));
        stmt.setBoolean(4, disponible);
        stmt.setString(5, nouveauGenre);
        stmt.setInt(6, idLivre);

        stmt.executeUpdate();
        JOptionPane.showMessageDialog(this, "Livre modifié avec succès !");
        loadBooks(); // Recharge les données dans le tableau
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "L'année doit être un nombre !");
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Erreur lors de la modification : " + ex.getMessage());
        ex.printStackTrace();
    }


    }//GEN-LAST:event_btnModifierActionPerformed

